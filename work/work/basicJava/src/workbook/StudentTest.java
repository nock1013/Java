package workbook;

public class StudentTest {

	public static void main(String[] args) {
		Student01[] students = new Student01[3];
		students[0] = new Student01("홍길동", 15, 171, 81);
		students[1] = new Student01("한사람", 13, 183, 72);
		students[2] = new Student01("홍길동", 16, 175, 65);
		
		int avgAge = 0;
		int avgHeight = 0;
		int avgWeight = 0;
		
		System.out.println("이름\t나이\t신장\t몸무게");
		for (int i = 0; i < students.length; i++) {
			students[i].print();
			avgAge += students[i].getAge();
			avgHeight += students[i].getHeight();
			avgWeight += students[i].getWeight();
		}
		
		System.out.println("나이의 평균: " + Math.round((float)avgAge / students.length * 100) / 100.0);
		System.out.println("신장의 평균: " + Math.round((float)avgHeight / students.length * 100) / 100.0);
		System.out.println("몸무게의 평균: " + Math.round((float)avgWeight / students.length* 100) / 100.0);
	}

}
