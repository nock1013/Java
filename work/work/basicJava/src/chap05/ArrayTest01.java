package chap05;
// 배열의 문법 연습 - 사용하는 방법
public class ArrayTest01 {

	public static void main(String[] args) {
		// 1. 배열의 선언
		int[] arr;
		int[] arr2;
		// 2. 배열의 생성
		arr = new int[5];
		arr2 = new int[5];
		
		System.out.println(arr);
		System.out.println(arr2);
		System.out.println("arr의 2번 요소의 값 => " + arr[2]);
		
		// 3. 배열의 초기화 - 2번 요소에 100을 저장
		arr[2] = 100;
		System.out.println("arr의 2번 요소의 값 => " + arr[2]);
		
		// 배열의 선언과 생성을 한꺼번에 처리
		int[] numArray = new int[3];
		boolean[] boolArr = new boolean[5];
		long[] longArr = new long[100];
		double[] doubleArr = new double[100];
		
		// 배열의 기본 초기값
		// 기본형인 경우 
		// 정수형 - 0
		// boolean - false
		// 실수형 - 0.0
		System.out.println(numArray[0]);
		System.out.println(boolArr[0]);
		System.out.println(longArr[0]);
		System.out.println(doubleArr[0]);
	}

}
